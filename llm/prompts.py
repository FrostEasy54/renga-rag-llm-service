from models.building import BuildingData

SYSTEM_PROMPT = """Ты — эксперт по календарному планированию строительных проектов.
Ты помогаешь составлять календарные планы на основе данных о здании.
Всегда отвечай только на русском языке.
Когда тебя просят составить календарный план, возвращай ответ строго в формате JSON.
"""


def build_schedule_prompt(building: BuildingData, rag_context: str = "") -> str:
    """
    Convert BuildingData into a detailed Russian prompt for the LLM.

    Sequencing follows МДС 12-43.2008's four-stage model:
      I.   Подготовительный период
      II.  Подземная часть (нулевой цикл)
      III. Надземная часть (возведение коробки) — поэтажно
      IV.  Отделочные работы

    Key real-world construction rules encoded here:
    - Per-floor sequence covers ONLY walls + slab. Windows are NOT installed
      per-floor — they would be damaged by ongoing structural work above.
    - Windows + external doors are installed once, after the roof, as a
      single "thermal envelope closure" milestone.
    - Engineering systems split into rough phase (before plaster) and fine
      phase (after painting).
    - Internal finishing and facade work run in parallel.
    - rag_context, if provided, is injected at the top of the prompt so the
      LLM treats retrieved norms as grounding material before reading the task.
    """
    if building.elements:
        lines: list[str] = [
            f"  - {el.count}x {el.type} ({el.material}, объём: {el.volume} м³)"
            for el in building.elements
        ]
        elements_text = "Элементы здания:\n" + "\n".join(lines)
    else:
        elements_text = "Элементы здания: данные не предоставлены."

    # Per-floor sequence is ONLY walls + slab — windows are a single step
    # AFTER the roof (closing the thermal envelope), not per-floor.
    floor_steps: list[str] = []
    for floor in range(1, building.floors + 1):
        floor_steps.append(
            f"     • Возведение стен {floor}-го этажа (несущие конструкции и перегородки)"
        )
        floor_steps.append(
            f"     • Устройство перекрытия {floor}-го этажа "
            f"(опирается на стены {floor}-го этажа)"
        )
    floor_sequence = "\n".join(floor_steps)

    context_block = _format_context_block(rag_context)

    return f"""{context_block}Составь календарный план строительства следующего объекта.

Название проекта: {building.name}
Тип здания: {building.building_type}
Количество этажей: {building.floors}
Общая площадь: {building.total_area} м²
{elements_text}
Дополнительные заметки: {building.notes or 'нет'}

Календарный план должен включать ЧЕТЫРЕ основных этапа согласно МДС 12-43.2008:

═══════════════════════════════════════════════════════════
I. ПОДГОТОВИТЕЛЬНЫЙ ПЕРИОД
═══════════════════════════════════════════════════════════
   • Подготовка и ограждение строительной площадки
   • Устройство временных сооружений и подключение временных коммуникаций
   • Геодезическая разбивка осей здания

═══════════════════════════════════════════════════════════
II. ПОДЗЕМНАЯ ЧАСТЬ (нулевой цикл)
═══════════════════════════════════════════════════════════
   • Земляные работы (разработка котлована или траншей)
   • Устройство фундамента
   • Гидроизоляция фундамента и обратная засыпка пазух котлована

═══════════════════════════════════════════════════════════
III. НАДЗЕМНАЯ ЧАСТЬ (возведение коробки)
═══════════════════════════════════════════════════════════
Поэтажная последовательность — ТОЛЬКО стены и перекрытия:
{floor_sequence}
   • Кровельные работы (стропильная система или плиты покрытия + кровельный пирог)

═══════════════════════════════════════════════════════════
IV. ОТДЕЛОЧНЫЕ РАБОТЫ
═══════════════════════════════════════════════════════════
   • Монтаж оконных и наружных дверных блоков — ЗАКРЫТИЕ ТЕПЛОВОГО КОНТУРА
     (выполняется ОДНИМ этапом после кровли, не по этажам)
   • Прокладка инженерных сетей, черновой этап (электрика, водопровод,
     канализация, стояки отопления — прячутся в штробы и перекрытия)
   • Штукатурные работы (внутренние)
   • Устройство полов (стяжка, плитка, ламинат)
   • Малярные и обойные работы
   • Установка инженерного оборудования, чистовой этап (радиаторы,
     сантехнические приборы, розетки, выключатели, светильники)
   • Отделка фасада (утепление, финишная облицовка) — может идти параллельно
     с внутренней отделкой
   • Благоустройство территории (отмостка, дорожки, озеленение)

═══════════════════════════════════════════════════════════
КРИТИЧЕСКИЕ ПРАВИЛА ПОСЛЕДОВАТЕЛЬНОСТИ
═══════════════════════════════════════════════════════════
1. Стены каждого этажа возводятся ДО перекрытия этого же этажа.
2. Перекрытие опирается на стены этажа — нельзя бетонировать перекрытие
   до возведения стен.
3. Каждый следующий этаж начинается ТОЛЬКО после устройства перекрытия
   предыдущего.
4. Кровля выполняется ПОСЛЕ перекрытия верхнего этажа.
5. Окна и наружные двери монтируются ОДНИМ этапом ПОСЛЕ кровельных работ.
   ЗАПРЕЩЕНО устанавливать окна поэтажно в процессе возведения коробки —
   они будут повреждены при дальнейших монтажных и кладочных работах
   на верхних этажах.
6. Внутренняя отделка начинается ТОЛЬКО после закрытия теплового контура
   (стены + кровля + окна + наружные двери) — иначе материалы пострадают
   от осадков и низких температур.
7. Черновые инженерные сети прокладываются ДО штукатурных работ —
   трубы и кабели должны быть скрыты в штробы и стяжку.
8. Чистовые инженерные работы (установка радиаторов, сантехники,
   электроарматуры, светильников) выполняются ПОСЛЕ малярных работ.
9. Отделка фасада может выполняться ПАРАЛЛЕЛЬНО с внутренней отделкой —
   это разные бригады, работающие независимо.
10. Благоустройство территории — заключительный этап перед сдачей объекта.

═══════════════════════════════════════════════════════════
ТРЕБОВАНИЯ К ФОРМИРОВАНИЮ ЗАДАЧ
═══════════════════════════════════════════════════════════
- task_name должен быть коротким и понятным, без скобок и технических деталей.
- depends_on должен точно отражать технологические зависимости (см. правила выше).
- workers_required — количество рабочих в ОДНОЙ смене (не суммарно по сменам).
- work_shifts — количество смен в сутки: обычно 2; 1 для работ с
  технологическими перерывами (например, набор прочности бетона);
  3 для непрерывного бетонирования больших объёмов.

Верни ответ ТОЛЬКО в формате JSON без дополнительного текста, строго по
следующей схеме:
{{
  "project_name": "...",
  "total_duration_days": <число>,
  "tasks": [
    {{
      "task_name": "...",
      "duration_days": <целое число>,
      "depends_on": ["название предыдущей задачи"],
      "workers_required": <число>,
      "work_shifts": <целое число>,
      "description": "..."
    }}
  ],
  "notes": "..."
}}
"""


# --------------------------------------------------------------------------- #
# Private helpers
# --------------------------------------------------------------------------- #

def _format_context_block(rag_context: str) -> str:
    """
    Wrap the RAG context in a clear instruction block placed before the task.
    Returns an empty string if there is no context, keeping the prompt clean.
    """
    if not rag_context:
        return ""
    return (
        f"{rag_context}\n\n"
        "Используй приведённые выше нормы при определении сроков и "
        "последовательности работ. Если норма указывает конкретную "
        "продолжительность — придерживайся её.\n\n"
    )